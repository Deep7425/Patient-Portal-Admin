	<h2 style=" line-height:40px;">Sputnik V, Covishield, Covaxin: <br />Everything about COVID-19 Vaccines</h2>

<p>India will now began administration of COVID-19 vaccines for everyone above 18 years of age from 1st may 2021.</p>
<p style=" margin-bottom:10px;"><strong>As of April 2021, 13 vaccines are authorized by at least one national regulatory authority for public use:</strong></p>
<ul>
	<li>Two RNA vaccines (the Pfizer–BioNTech vaccine and the Moderna vaccine), five conventional inactivated vaccines (BBIBP-CorV, CoronaVac, Covaxin, WIBP-CorV and CoviVac)</li>
	<li>Four viral vector vaccines (Sputnik V, the Oxford–AstraZeneca vaccine, Convidecia, and the Johnson & Johnson vaccine)</li>
	<li>Two protein subunit vaccines (EpiVacCorona and RBD-Dimer).</li></ul>

<p>In total, as of March 2021, 308 vaccine candidates are in various stages of development, with 73 in clinical research, including 24 in Phase I trials, 33 in Phase I–II trials, and 16 in Phase III development.[3]</p>

<p>Two vaccines received approval for emergency use in India at the onset of the program, including Covishield (a version of the Oxford–AstraZeneca vaccine manufactured by the Serum Institute of India), and Covaxin (developed by Bharat Biotech). In April 2021, Sputnik V was approved as a third vaccine, with deployment expected to begin by late-May 2021.</p>

<h2 style=" margin:15px 0 10px 0;">VACCINE TYPES</h2>
<h2 style=" margin:20px 0 10px 0">RNA Vaccines</h2>
<ul>

<li>An RNA vaccine contains RNA which, when introduced into a tissue, acts as messenger RNA (mRNA) to cause the cells to build the foreign protein and stimulate an adaptive immune response which teaches the body how to identify and destroy the corresponding pathogen or cancer cells. </li>
<li>
RNA vaccines were the first COVID-19 vaccines to be authorized in the United States and the European Union. As of January 2021, authorized vaccines of this type are the Pfizer–BioNTech COVID-19 vaccine and the Moderna COVID-19 vaccine. </li>
</ul>

<h2 style=" margin:10px 0 10px 0">Adenovirus vector vaccines</h2>
<ul>
<li>These vaccines are examples of non-replicating viral vector vaccines, using an adenovirus shell containing DNA that encodes a SARS CoV 2 protein. The viral vector-based vaccines against COVID-19 are non-replicating, meaning that they do not make new virus particles, but rather produce only the antigen which elicits a systemic immune response.[104]</li>

<li>As of January 2021, authorized vaccines of this type are the Oxford–AstraZeneca COVID-19 vaccine, the Sputnik V COVID-19 vaccine, Convidecia, and the Johnson & Johnson COVID-19 vaccine.</li>
<li>Convidecia and the Johnson & Johnson COVID-19 vaccine are both one-shot vaccines which offer less complicated logistics and can be stored under ordinary refrigeration for several months. </li>
<li>The Sputnik V COVID-19 vaccine uses Ad26 for the first dose, which is the same as the Johnson & Johnson vaccine's only dose, and Ad5 for the second dose. Convidecia uses Ad5 for its only dose.</li>

<h3>Inactivated virus vaccines</h3>
<li>Inactivated vaccines consist of virus particles that have been grown in culture and then are killed using a method such as heat or formaldehyde to lose disease producing capacity, while still stimulating an immune response. </li>
<li>As of January 2021, authorized vaccines of this type are the Chinese CoronaVac, BBIBP-CorV, and WIBP-CorV; the Indian Covaxin; and the Russian CoviVac. Vaccines in clinical trials include the Valneva COVID-19 vaccine.</li>

<h2 style=" margin:10px 0 10px 0">Subunit vaccines</h2>
<ul>
<li>
Subunit vaccines present one or more antigens without introducing whole pathogen particles. The antigens involved are often protein subunits, but can be any molecule that is a fragment of the pathogen. </li>
<li>As of April 2021, the two authorized vaccines of this type are the peptide vaccine EpiVacCorona and RBD-Dimer. Vaccines with pending authorizations include the Novavax COVID-19 vaccine, SOBERANA 02 (a conjugate vaccine), and the Sanofi–GSK vaccine.</li>
</ul>

<h3 style=" margin:10px 0 10px 0">Other types</h3>
<ul>
	<li>Additional types of vaccines that are in clinical trials include virus-like particle vaccines, multiple DNA plasmid vaccines, at least two lentivirus 	vector vaccines, a conjugate vaccine, and a vesicular stomatitis virus displaying the SARS CoV 2 spike protein.</li>
	<li>Oral vaccines and intranasal vaccines are being developed and studied. </li>
</ul>
 
<h2 style=" margin:10px 0 10px 0">COVIESHEILD VACCINE</h2>
<ul>
	<li>The Oxford-AstraZeneca vaccine is being manufactured locally by the Serum Institute of India. The vaccine is made from a weakened version of a common cold virus (known as an adenovirus) from chimpanzees. It has been modified to look more like coronavirus - although it can't cause illness.</li>
<li>When the vaccine is injected into a patient, it prompts the immune system to start making antibodies and primes it to attack any coronavirus infection.</li>
<li>The jab is administered in two doses given between four and 12 weeks apart. It can be safely stored at temperatures of 2C to 8C and can easily be delivered in existing health care settings such as doctors' surgeries.</li>
<li>COMPOSITION: Composition of Covishield includes inactivated adenovirus with segments of Coronavirus, Aluminium Hydroxide Gel, L-Histidine, L-Histidine Hydrochloride Monohydrate, Magnesium Chloride Hexahydrate, Polysorbate 80, Ethanol, Sucrose, Sodium Chloride, and Disodium Edetate Dihydrate (EDTA)</li>

<h3>How effective is Covishield?</h3>
<ul>
<li>International clinical trials of the Oxford-AstraZeneca vaccine showed that when people were given a half dose and then a full dose, effectiveness hit 70- 90%.
</li>
</ul>

<h3>COMMON SIDE EFFETCS</h3>
<p>Covishield Side-effects: Some mild symptoms that may occur include</p>
<ul>
<li>Pain and tenderness at the site of injection site</li>
<li>Headache</li>
<li>Fatigue</li>
<li>Myalgia (muscle pain)</li>
<li>Malaise (Generalised weakness)</li>
<li>Pyrexia (fever)</li>
<li>Chills</li>
<li>Arthralgia (joint pain)</li>
<li>Nausea</li>
</ul>

<p>In very rare cases, neurological complications (demyelinating disorders) have been reported following vaccination with this vaccine</p>

<h2 style=" margin:10px 0 10px 0"> COVAXIN VACCINE</h2>
<ul>
<li>Covaxin is an inactivated vaccine which means that it is made up of killed coronaviruses, making it safe to be injected into the body.</li>
<li>Bharat Biotech, a 24-year-old vaccine maker with a portfolio of 16 vaccines and exports to 123 countries, used a sample of the coronavirus, isolated by India's National Institute of Virology.</li>
<li>When administered, immune cells can still recognise the dead virus, prompting the immune system to make antibodies against the pandemic virus.</li>
<li>The two doses are given four weeks apart. The vaccine can be stored at 2C to 8C.</li>
<li>COMPOSITION :- Composition of Covaxin includes inactivated Coronavirus, Aluminum Hydroxide Gel, TLR 7/8 Agonist, 2-Phenoxyethanol and Phosphate Buffered Saline [NKA1]</li>
</ul>

<h3>How effective is Covaxin?</h3>

<ul>
<li>The vaccine has an efficacy rate of 78-81%, preliminary data from its phase 3 trial shows.</li>
</ul>

<h3>COMMON SIDE EFFETCS</h3>
<p>Covaxin Side-effects: Some immediate symptoms that may occur include</p>
<ul>
    <li>Pain and swelling at the site of the injection</li>
    <li>Headache</li>
    <li>Fatigue</li>
    <li>Fever</li>
    <li>Body ache</li>
    <li>Abdominal pain</li>
    <li>Nausea and vomiting</li>
    <li>Dizziness-giddiness</li>
    <li>Sweating</li>
    <li>Cold and cough</li>
    <li>Tremors</li>
</ul>
<p>No other vaccine-related serious adverse effects have been reported.</p>


<h2 style=" margin:10px 0 10px 0px;">SPUTNIK V</h2>

<ul>
<li>The vaccine, developed by Moscow's Gamaleya Institute. It uses a cold-type virus, engineered to be harmless, as a carrier to deliver a small fragment of the coronavirus to the body.</li>
<li>Safely exposing the body to a part of the virus's genetic code in this way allows it to recognise the threat and learn to fight it off, without the risk of becoming ill.</li>
<li>After being vaccinated, the body starts to produce antibodies especially tailored to the coronavirus.</li>
<li>This means that the immune system is primed to fight coronavirus when it encounters it for real.</li>
<li>It can be stored at temperatures of between 2 and 8C degrees (a standard fridge is roughly 3-5C degrees) making it easier to transport and store.
But it has a different second dose,</li>
<li>Unlike other similar vaccines, the Sputnik jab uses two slightly different versions of the vaccine for the first and the second dose - given 21 days apart.</li>
<li>They both target the coronavirus's distinctive "spike", but use different vectors - the neutralised virus that carries the spike to the body.</li>
<li>The idea is that using two different formulas boosts the immune system even more than using the same version twice - and may give longer-lasting protection.</li>
</ul>

<h3>COMMON SIDE EFFETCS</h3>

<ul>
	<li>Some side-effects to a vaccine are expected, but these are usually mild, including a sore arm, tiredness and a bit of a temperature. There were no deaths or serious illnesses in the vaccinated group linked to the jab.</li>
	<li>When will Sputnik V be available in India?</li>
	<li>Hyderabad-based pharmaceutical major Dr Reddy's Laboratories will be importing the first batch of 125 million doses to India during this quarter.</li>
</ul>


<h3>Who Should Get Vaccinated?</h3>
<p>According to the current guidelines, anyone above 45 years of age should get vaccinated without fail. But, from 1st May 2021, every person above 18 years of age should be vaccinated.</p>
 
<h3>Who Should NOT Take The Vaccine?</h3>
<strong>1. COVID-19 vaccination is contraindicated in</strong>
<ul>
	<li>People with allergic reaction/anaphylaxis (immediate or delayed) to vaccines, food items, pharmaceutical products, injectables, etc</li>
	<li>People with a history of allergic reaction to a previous dose of the COVID-19 vaccine</li>
	<li>Women who are pregnant (including the ones who are not sure of their pregnancy) and breastfeeding women </li>
</ul>

<strong>2. COVID-19 vaccination is temporarily contraindicated (deferred for 4-8 weeks or post-recovery) in people</strong>
<ul>
	<li>With active COVID-19 infection or those waiting for test results
	<li>Who have been given anti-COVID-19 monoclonal antibodies or convalescent plasma 
	<li>Who are unwell and hospitalized for any illness
</ul>

<ul>
	<li>Covishield is to be given with caution in people with low platelet count (thrombocytopenia) and Covaxin when given to people taking drugs such as chloroquine and/or corticosteroids is known to affect the antibody response, so talk to your doctor before getting vaccinated.</li>
</ul>

<h3>Post Vaccination Tips To Follow</h3>
<p><strong>1. Follow COVID Appropriate Behaviour (CAB) such as:</strong></p>
<ul>
<li>Wearing a mask</li>
<li>Using a hand sanitiser</li>
<li>Washing hands with soap and water</li>
<li>Maintaining social distance (physical distance of at least 2 feet)</li>


<p>If you experience minor side-effects such as pain or discomfort post-vaccination, taking over-the-counter (OTC) medications such as paracetamol and painkillers after consultation with a doctor is advised.</p>

<p>You can even apply a cold cloth over the site of injection or exercise your arm to reduce pain and discomfort. Drink loads of fluids and dress lightly to reduce discomfort from fever.</p>

<p>3. If you are under self-isolation, waiting for the COVID-19 test results or are ill on the date of the vaccination or have symptoms of COVID-19, cancel your appointment. You can reschedule your appointment after 4-8 weeks of recovery from COVID symptoms.</p>
<ul>
<li>Suffering from cough/cold, fever and breathing difficulty. Book a test .</li></ul>

<p>Make sure to take the second dose of the vaccine without fail. Also ensure, the second dose is the same as the first administered dose. For example, if your first vaccine dose is Covishield, then take Covishield in the second dose as well. Do not take Covaxin during the second dose and vice-versa. So if you are taking your first dose, then don’t forget to ask your vaccinator about the type of vaccine.</p>


<h3 style=" margin:12px 0 12px 0; width:100%; float:left;">REGISTER YOURSELF FOR COVID VACCINE</h3>


<p>How to register for Covid-19 vaccination via CoWIN portal / App</p>
<ul>
    <li>Log on to www.cowin.gov.in App</li>
    <li>Enter your mobile number.</li>
    <li>Get an OTP to create your account.</li>
    <li>Enter the OTP and click on the “Verify" button.</li>
    <li>You will be directed to the registration of the vaccination' page. On this page, there will be an option to choose one photo ID proof.</li>
    <li>Fill in your name, age, gender and upload an identity document.</li>
    <li>Click on the “Register" button.</li>
    <li>Once the registration is completed; the system will show the “Account Details".</li>
    <li>A citizen can further add three more people linked with this mobile number by clicking on the “Add More" button.</li>
    <li>There will be a button indicating 'Schedule appointment'. Now click on it.</li>
    <li>Search a vaccination centre of choice by state, district, block and pin code.</li>
    <li>Date and availability will also be displayed.</li>
    <li>Click on the 'book' button.</li>
    <li>On successful completion of booking, you will receive a message. That confirmation details will have to be shown at the vaccination centre.</li>
</ul>


<p style=" margin:0 0 15px 0"><strong>Keynotes to remember, when going for COVID vaccination -</strong></p>
<ul>
	<li>1. Please Carry the Photo ID card mentioned in your appointment details for Covid vaccination</li>
	<li>2. If you have any comorbidities, please carry a medical certificate with you for the vaccination appointment</li>
	<li>3. You can also call the CoWIN helpline number 1075 for any additional details</li>
</ul>

    
    
    
