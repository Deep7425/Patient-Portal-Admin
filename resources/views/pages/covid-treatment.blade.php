        <h2>MILD</h2>
        <h3>Symptoms:</h3>
        <ul>
          <li>Low-grade fever (not more than 100 degrees Fahrenheit)</li>
          <li>Dry cough</li>
          <li>Fatigue</li>
          <li>Headache</li>
          <li>New loss of taste or smell</li>
          <li>Gastrointestinal upset, including vomiting and diarrhea </li>
          <li>Itchy, painful patches on skin (especially in young people). These patches often show up on the toes and have been referred to as “COVID toes.” </li>
        </ul>
